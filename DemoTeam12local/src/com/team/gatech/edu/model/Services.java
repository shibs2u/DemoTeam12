package com.team.gatech.edu.model;

public class Services {
	
	int foodpantryId;
	int soupkitchenid;
	int shelterid;
	
	public int getFoodpantryId() {
		return foodpantryId;
	}
	public void setFoodpantryId(int foodpantryId) {
		this.foodpantryId = foodpantryId;
	}
	public int getSoupkitchenid() {
		return soupkitchenid;
	}
	public void setSoupkitchenid(int soupkitchenid) {
		this.soupkitchenid = soupkitchenid;
	}
	public int getShelterid() {
		return shelterid;
	}
	public void setShelterid(int shelterid) {
		this.shelterid = shelterid;
	}
	

}
